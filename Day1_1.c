// Sum of two numbers
#include<stdio.h>
int main()
{
    int n1,n2,sum=0;
    printf("Enter the value of first number :  ");
    scanf("%d", &n1);
    printf("Enter the value of second number :  ");
    scanf("%d", &n2);
    sum = n1+n2;
    printf("Sum of two numbers is :  %d ",sum);
    return 0;
    

}